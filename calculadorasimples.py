# Calculadora Simples: um programa que realiza operações básicas de matématica.

n1 = int(input('digite o primeiro número: '))
n2 = int(input('digte o segundo número: '))

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2

print('soma: {}'.format(soma))
print('subtração: {}'.format(subtracao))
print('multiplicação: {}'.format(multiplicacao))
print('divisão: {}'.format(divisao))